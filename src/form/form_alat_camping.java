    protected void datatable(){
        Object[] Baris = {"Kode Alat", "Nama Alat", "Merk", "Kondisi", "Kelengkaoan", "Harga", "Stock"};
            model = new DefaultTableModel(null, Baris);
            String cariitem=txtcari.getText();
            
            try {
                String sql = "SELECT * FROM alat_camping WHERE " +
                    "kd_alat LIKE '%" + cariitem + "%' OR " +
                    "nama_alat LIKE '%" + cariitem + "%' OR " +
                    "merk LIKE '%" + cariitem + "%' OR " +
                    "kondisi LIKE '%" + cariitem + "%' OR " +
                    "kelengkapan LIKE '%" + cariitem + "%' OR " +
                    "harga_sewa LIKE '%" + cariitem + "%' OR " +
                    "stok LIKE '%" + cariitem + "%' " +
                    "ORDER BY kd_alat ASC";
                Statement stat = koneksi.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                
                while (hasil.next()){
                    model.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6),
                        hasil.getString(7),
                    });
                }
                tablealat.setModel(model);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal dipanggil"+e);
            }
    }
    
    private String generateKodeAlat(){
        String prefix = "KD";
        String sql = "SELECT kd_alat FROM alat_camping ORDER BY kd_alat DESC LIMIT 1";
        try {
            Statement stat = koneksi.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            if (rs.next()) {
                String lastId = rs.getString("kd_alat");
                int number = Integer.parseInt(lastId.substring(2));
                number++;
                return String.format(prefix + "%03d", number);
            } else {
                return prefix + "001";
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal generate Kode: " + e);
            return null;
        }
    }

